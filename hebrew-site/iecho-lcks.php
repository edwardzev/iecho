<script>
document.addEventListener('DOMContentLoaded', function() {
  const contactForm = document.getElementById('contact-form');
  if (!contactForm) return;

  const messagesDiv = contactForm.querySelector('.messages');
  const successText = 'Спасибо за обращение! Мы свяжемся с Вами в ближайшее время.';
  
  // ===== SESSION STORAGE VERSION =====
  function getGoalSent() {
    return sessionStorage.getItem('ymGoalSent') === 'true';
  }
  
  function setGoalSent() {
    sessionStorage.setItem('ymGoalSent', 'true');
  }
  // ===================================

  function triggerMetricaGoal() {
    if (getGoalSent()) return;
    
    if (typeof ym !== 'undefined') {
      ym(66079705, 'reachGoal', 'iechosuccess');
      console.log('✅ Yandex Metrica: Success goal triggered (first time)');
      setGoalSent(); // Mark as sent in session storage
    } else {
      console.warn('⚠️ Yandex Metrica not loaded');
    }
  }

  function checkForSuccess() {
    if (getGoalSent()) return;
    
    if (messagesDiv && messagesDiv.style.display === 'block' && 
        messagesDiv.textContent.includes(successText)) {
      triggerMetricaGoal();
    }
  }

  // Initial check
  checkForSuccess();

  // Set up observer
  if (messagesDiv) {
    new MutationObserver(function(mutations) {
      if (getGoalSent()) return;
      mutations.forEach(function(mutation) {
        if (mutation.attributeName === 'style') {
          checkForSuccess();
        }
      });
    }).observe(messagesDiv, {attributes: true});
  }

  // Clear flag when form is submitted again
  contactForm.addEventListener('submit', function() {
    sessionStorage.removeItem('ymGoalSent'); // Reset on new submission
    setTimeout(checkForSuccess, 400);
  });
});
</script>
