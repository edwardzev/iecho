      setGoalSent(); // Mark as sent in session storage
    } else {
      console.warn('⚠️ Yandex Metrica not loaded');
    }
  }

  function checkForSuccess() {
    if (getGoalSent()) return;
    
    if (messagesDiv && messagesDiv.style.display === 'block' && 
        messagesDiv.textContent.includes(successText)) {
      triggerMetricaGoal();
    }
  }

  // Initial check
  checkForSuccess();

  // Set up observer
  if (messagesDiv) {
    new MutationObserver(function(mutations) {
      if (getGoalSent()) return;
      mutations.forEach(function(mutation) {
        if (mutation.attributeName === 'style') {
          checkForSuccess();
        }
      });
    }).observe(messagesDiv, {attributes: true});
  }

  // Clear flag when form is submitted again
  contactForm.addEventListener('submit', function() {
    sessionStorage.removeItem('ymGoalSent'); // Reset on new submission
    setTimeout(checkForSuccess, 400);
  });
});
