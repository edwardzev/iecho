  const messagesDiv = contactForm.querySelector('.messages');
  const successText = 'Спасибо за обращение! Мы свяжемся с Вами в ближайшее время.';
  
  // ===== SESSION STORAGE VERSION =====
  function getGoalSent() {
    return sessionStorage.getItem('ymGoalSent') === 'true';
  }
  
  function setGoalSent() {
    sessionStorage.setItem('ymGoalSent', 'true');
  }
  // ===================================

  function triggerMetricaGoal() {
    if (getGoalSent()) return;
    
    if (typeof ym !== 'undefined') {
      ym(66079705, 'reachGoal', 'iechosuccess');
